    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       
        String id1=id.getText();
        int id2 = Integer.parseInt(id1);
        String name1=name.getText();
        String fname1=fname.getText();
        String age1=age.getText();
        int age2=Integer.parseInt(age1);
        String address1=address.getText();
        String disease1=disease.getText();
        String contact1=contact.getText();
        String date1=date.getText();
        if("".equals(id1)||"".equals(name1)||"".equals(fname1)||"".equals(age1)||"".equals(address1)||"".equals(disease1)||"".equals(contact1)||"".equals(date1))
        {
            JOptionPane.showMessageDialog(null,"Please enter all details");
        }
        else
        {
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql:///hospital","root","");
            String query="insert into patient values(?,?,?,?,?,?,?,?,?)";
            PreparedStatement pst=con.prepareStatement(query);
            pst.setInt(1,id2);
            pst.setString(2,name1);
            pst.setString(3,genders);
            pst.setString(4,fname1);
            pst.setInt(5,age2);
            pst.setString(6,address1);
            pst.setString(7,disease1);
            pst.setString(8,contact1);
            pst.setString(9,date1);
            int rs=pst.executeUpdate();
            if(rs==1)
                JOptionPane.showMessageDialog(null,"DONE");
            else
                JOptionPane.showMessageDialog(null,"NOT DONE");
        }
        catch(Exception ae)
        {
            JOptionPane.showMessageDialog(null, ae.getMessage());
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

